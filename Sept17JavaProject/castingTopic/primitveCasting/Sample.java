package primitveCasting;

public class Sample {
	
	
	public static void main(String args[])
	{
		byte a = 120;
		System.out.println(a);
		
		short b = a;
		System.out.println(b);
		
		int c = b;
		System.out.println(c);
		
		long d = c;
		System.out.println(d);
		
		float e = d;
		System.out.println(e);
		
		double f = e;
		System.out.println(f);	
		
	}
}
