package datatypesExamples;

public class Example1 {

	public static void main(String args[])
	{
		boolean r = 10>20;
		System.out.println(r);
		
		//------ byte datatype -----
		byte a = 100;
		byte b = -128;
		System.out.println(a);
		System.out.println(b);
		
		//---------- short datatype ----
		short c = 129;
		System.out.println(c);
		//----------- int datatype
		int d = 32769;
		System.out.println(d);
	}
}
