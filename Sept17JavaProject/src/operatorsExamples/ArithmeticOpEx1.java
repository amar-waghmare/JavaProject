package operatorsExamples;

public class ArithmeticOpEx1 {

	public static void main(String[] args) {
		
		double a = 80;
		int b = 15;
		
		System.out.println(a+b); // 95
		System.out.println(a-b); // 65
		System.out.println(a*b); // 1200
		System.out.println(a/b); // 5
		System.out.println(a%b);  // 5
	}
}
